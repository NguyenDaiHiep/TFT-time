package Bai1;

public class Display {
	private int size;
	private int numberOfColor;

	public Display(int size, int numberOfColor) {
		this.size = size;
		this.numberOfColor = numberOfColor;
	}

	@Override
	public String toString() {
		return "Display [size=" + size + ", numberOfColor=" + numberOfColor + "]";
	}
	

}
